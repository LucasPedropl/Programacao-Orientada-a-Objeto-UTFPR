// i) Classe Byte: métodos converStr() e byteValue()
// ii) O método converStr vai converter uma string para byte.
// iii) O método byteValue vai retornar o valor primitivo de Byte.
// iv) Biblioteca Java

public class TstByte {
    public Byte converStr(String Str) {
        return Byte.decode(Str);
    }

    public Byte Byteparabyte(Byte bit){
        return bit.byteValue();
    }
}
